package com.example.tiktaktoe.patterns;

/**
 * A guide to common design patterns in Java.
 * This class contains descriptions and examples of various design patterns.
 */
public class Documentation_about_all_of_he_Pattern {


    /**
     * Proxy Pattern:
     * Category: Structural
     * Definition: Provides a surrogate or placeholder for another object to control access to it.
     * Example in Java: Java RMI (Remote Method Invocation), where local stubs act as proxies for remote objects.
     */

    /**
     * Adapter Pattern:
     * Category: Structural
     * Definition: Converts the interface of a class into another interface that clients expect.
     *             Adapter lets classes work together that couldn't otherwise because of incompatible interfaces.
     * Example in Java: Arrays.asList() method converts an array to a List.
     */


    /**
     * Composite Pattern:
     * Category: Structural
     * Definition: Composes objects into tree structures to represent part-whole hierarchies.
     *             Composite lets clients treat individual objects and compositions of objects uniformly.
     * Example in Java: Swing's container classes like JPanel, which can contain other components.
     */


    /**
     * Decorator Pattern:
     * Category: Structural
     * Definition: Attaches additional responsibilities to an object dynamically.
     *             Decorators provide a flexible alternative to subclassing for extending functionality.
     * Example in Java: Java I/O classes, such as FileInputStream, wrapped by BufferedInputStream.
     */


    /**
     * Observer Pattern:
     * Category: Behavioral
     * Definition: Defines a one-to-many dependency between objects so that when one object changes state,
     *             all its dependents are notified and updated automatically.
     * Example in Java: Event handling in GUI components, like JButton's addActionListener method.
     */


    /**
     * Visitor Pattern:
     * Category: Behavioral
     * Definition: Represents an operation to be performed on elements of an object structure.
     *             Visitor lets you define a new operation without changing the classes of the elements on which it operates.
     * Example in Java: Java Compiler API, where visitors can traverse syntax trees and perform operations.
     */
    

    /**
     * Iterator Pattern:
     * Category: Behavioral
     * Definition: Provides a way to access the elements of an aggregate object sequentially without exposing its underlying representation.
     * Example in Java: Iterator in the Java Collections Framework to traverse collections.
     */


    /**
     * Singleton Pattern:
     * Category: Creational
     * Definition: Ensures a class has only one instance and provides a global point of access to it.
     * Example in Java: java.lang.Runtime.getRuntime(), which returns a singleton runtime object.
     */



}
